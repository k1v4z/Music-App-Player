package com.example.musicapp.model;

public class ArtistOfYear {
    private String id;
    private String image;

    public ArtistOfYear(String id, String image) {
        this.id = id;
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
